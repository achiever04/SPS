def load_csv_data(csv_path: str) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    """
    Load OHLCV data from a CSV file.

    Expects columns: date, open, high, low, close, volume
    Optional columns: oi (open interest)

    Returns:
        prices:  (N,) array of close prices
        volumes: (N,) array of volumes
        oi_arr:  (N,) array of open interest (zeros if not present)
    """
    import pandas as pd

    print(f"📂 Loading data from: {csv_path}")
    df = pd.read_csv(csv_path, parse_dates=["date"])
    df = df.sort_values("date").reset_index(drop=True)

    required = {"close", "volume"}
    missing = required - set(df.columns)
    if missing:
        raise ValueError(f"CSV missing required columns: {missing}")

    prices  = df["close"].values.astype(np.float64)
    volumes = df["volume"].values.astype(np.float64)
    oi_arr  = df["oi"].values.astype(np.float64) if "oi" in df.columns else np.zeros_like(prices)

    print(f"   ✅ Loaded {len(prices):,} bars | "
          f"Range: {df['date'].iloc[0]} → {df['date'].iloc[-1]}")
    return prices, volumes, oi_arr


def generate_synthetic_data(
    n_samples: int = 15_000,
) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    """
    Synthetic data for quick testing only.
    NOT for production model training.
    """
    print("⚠️  Using SYNTHETIC data — replace with real OHLCV CSV for production!")
    np.random.seed(42)

    # Simulate a mean-reverting + trending price series
    returns = np.random.normal(0.0001, 0.005, n_samples + 300)
    prices  = 19_500 * np.cumprod(1 + returns)
    volumes = np.abs(np.random.normal(500_000, 150_000, len(prices)))
    oi_arr  = np.abs(np.random.normal(1_000_000, 200_000, len(prices)))

    return prices, volumes, oi_arr


