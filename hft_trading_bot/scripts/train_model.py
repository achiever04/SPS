"""
===============================================================================
  train_model.py — Train & Export Trading Signal Model to ONNX  (v2)
===============================================================================
  Complete rewrite from v1.  Key improvements:

  1. REAL DATA LOADER
     Loads OHLCV CSVs (from Zerodha Kite / Yahoo Finance / NSE) instead of
     synthetic random-walk data.

  2. CORRECT TIME-SERIES SPLIT
     Never shuffles historical data.  Uses a strict chronological
     train / validation / test split to prevent look-ahead bias.

  3. TRIPLE-BARRIER LABELING
     Labels are generated by the barrier method:
     BUY  (2) — price hits TP before SL within lookahead window
     SELL (0) — price hits SL before TP within lookahead window
     HOLD (1) — neither barrier hit within lookahead window

  4. LIGHTGBM CLASSIFIER
     LightGBM trains 10–50× faster than sklearn GBT and typically achieves
     higher accuracy on tabular financial data.  Falls back to sklearn
     GradientBoostingClassifier if lightgbm is not installed.

  5. CLASS BALANCING
     class_weight='balanced' handles the typical HOLD-heavy distribution.

  6. WALK-FORWARD CROSS-VALIDATION
     Trains on months 1–N, tests on month N+1, slides forward.
     Gives a realistic estimate of out-of-sample performance.

  7. CALIBRATED CONFIDENCE
     CalibratedClassifierCV ensures predict_proba outputs are proper
     probabilities (not just relative scores).

  8. FEATURE ENGINEERING matches FeatureEngine v2 exactly (19 features).

  Usage:
      # With real data CSV:
      python scripts/train_model.py --data data/NIFTY_1min.csv

      # Quick test with synthetic data (not for production):
      python scripts/train_model.py --synthetic

  CSV format expected (Zerodha Kite export):
      date,open,high,low,close,volume
      2024-01-01 09:15:00,19500,19510,19490,19505,12345

  Dependencies:
      pip install lightgbm scikit-learn skl2onnx onnxruntime joblib pandas
===============================================================================
"""

